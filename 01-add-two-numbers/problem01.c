#include <stdio.h>

int main() {
  int first_number, second_number;

  printf("Enter 1st number : ");
  scanf("%d", &first_number);
  printf("\n");

  printf("Enter 2nd number : ");
  scanf("%d", &second_number);
  printf("\n");

  printf("Sum = %d\n\n", first_number + second_number);

  return 0;
}
